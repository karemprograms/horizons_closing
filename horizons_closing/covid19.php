        <p class="content-part">
          Are a group of related RNA viruses that cause diseases
          in mammals and birds. In humans, these viruses cause respiratory tract
          infections that can range from mild to lethal. Mild illnesses include some
          cases of the common cold (which is also caused by other viruses,
          predominantly rhinoviruses), while more lethal varieties can
          cause SARS, MERS, and COVID-19. Symptoms in other species vary: in
          chickens, they cause an upper respiratory tract disease, while in cows and
          pigs they cause diarrhea. There are as yet no vaccines or antiviral drugs to
          prevent or treat human coronavirus infections.
        </p>
        <h4><b>Orthocoronavirinae</b></h4>
        <div class="row mt-3 m-3 text-center">
          <div class="col-sm mt-2">
            <figure class="figure">
              <img src="img/orthocoronavirinae.jpeg" class="figure-img img-fluid what-fig rounded">
