        <p class="content-part">
          In the family Coronaviridae, order Nidovirales, and realm Riboviria. They
          are enveloped viruses with a positive-sense singlestranded RNA genome and a nucleocapsid of helical
          symmetry.
          [7] The genome size of coronaviruses ranges from approximately
          26 to 32 kilobases, one of the largest among RNA viruses. They have
          characteristic club-shaped spikes that project from their surface, which
          in electron micrographs create an image reminiscent of the solar corona,
          from which their name derives
        </p>
        <h4><b>History</b></h4>
        <div class="row mt-3 m-3 text-center">
          <div class="col-sm mt-2">
            <figure class="figure">
              <img src="img/history.jpeg" class="figure-img img-fluid what-fig rounded">
