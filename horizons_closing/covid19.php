        <p class="content-part">
          <b>1-Structure:</b> Coronaviruses are large, roughly spherical, particles with bulbous
          surface projections.[41] The average diameter of the virus particles is around
          125 nm (.125 Î¼m). The diameter of the envelope is 85 nm and the spikes are
          20 nm long. The envelope of the virus in electron micrographs appears as a
          distinct pair of electron-dense shells (shells that are relatively opaque to the
          electron beam used to scan the virus particle)[42][43]
        </p>
        <p class="content-part">
          The viral envelope consists of a lipid bilayer, in which the membrane (M),
          envelope (E) and spike (S) structural proteins are anchored.[44] The ratio of E:S:M in the lipid bilayer is approximately 1:20:300. [45] On average a coronavirus particle
          has 74 surface spikes.
          [46] A subset of coronaviruses (specifically the members
          of betacoronavirus subgroup A) also have a shorter spike-like surface protein
          called hemagglutinin esterase (HE).
        </p>
        <p class="content-part">
          The coronavirus surface spikes are homotrimers of the S protein, which is
          composed of an S1 and S2 subunit. The homotrimeric S protein is a class I fusion
          protein which mediates the receptor binding and membrane fusion between the
          virus and host cell. The S1 subunit forms the head of the spike and has the
          receptor binding domain (RBD). The S2 subunit forms the stem which anchors the
          spike in the viral envelope and on protease activation enables fusion. The E and M
          protein are important in forming the viral envelope and maintaining its structural
          shape
        </p>
        <p class="content-part">
          Inside the envelope, there is the nucleocapsid, which is formed from multiple
          copies of the nucleocapsid (N) protein, which are bound to the positive-sense
          single-stranded RNA genome in a continuous beads-on-a-string type
          conformation. The lipid bilayer envelope, membrane proteins, and nucleocapsid
          protect the virus when it is outside the host cell.
        </p>
        <p class="content-part">
          <b>2-GenomeSee:</b> also Severe acute respiratory syndrome-related coronavirus Genome
        </p>
        <p class="content-part">
          Coronaviruses contain a positive-sense, single-stranded RNA genome.
          The genome size for coronaviruses ranges from 26.4 to 31.7 kilobases. The
          genome size is one of the largest among RNA viruses. The genome has a 5â€²
          methylated cap and a 3â€² polyadenylated tail.

        </p>
        <p class="content-part">
          The genome organization for a coronavirus is 5â€²-leader-UTRreplicase/transcriptase-spike (S)-envelope (E)-membrane (M)-nucleocapsid (N)-
          3â€²UTR-poly (A) tail. The open reading frames 1a and 1b, which occupy the first
          two-thirds of the genome, encode the replicase-transcriptase polyprotein (pp1ab).
          The replicase-transcriptase polyprotein self cleaves to form 16 nonstructural
          proteins (nsp1â€“nsp16).[43]
        </p>
        <p class="content-part">
          The later reading frames encode the four major structural proteins: spike,
          envelope, membrane, and nucleocapsid. Interspersed between these reading
          frames are the reading frames for the accessory proteins. The number of accessory
          proteins and their function is unique depending on the specific coronavirus.
        </p>

